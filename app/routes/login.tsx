import { LogIn } from "lucide-react";
import { useEffect, useState } from "react";
import { useNavigate } from "react-router";
import { Button } from "~/components/ui/button";
import { Card, CardContent, CardHeader } from "~/components/ui/card";
import { supabase } from "~/lib/client";
export function meta() {
	return [
		{ title: "Trip Planner - Login" },
		{
			name: "description",
			content: "Plan your perfect journey",
		},
	];
}
export default function Login() {
	const navigate = useNavigate();
	const [loading, setLoading] = useState(false);
	const [error, setError] = useState<string | null>(null);

	useEffect(() => {
		// Check if already logged in
		supabase.auth.getSession().then(({ data: { session } }) => {
			if (session) {
				navigate("/");
			}
		});
	}, [navigate]);

	const handleGoogleSignIn = async () => {
		setLoading(true);
		setError(null);

		try {
			const response = await fetch("/api/auth", {
				method: "POST",
				headers: { "Content-Type": "application/json" },
				body: JSON.stringify({ action: "google" }),
			});

			const data = await response.json();

			if (data.error) {
				setError(data.error);
				setLoading(false);
				return;
			}

			// Redirect to Google OAuth
			if (data.url) {
				window.location.href = data.url;
			}
		} catch {
			setError("Failed to sign in. Please try again.");
			setLoading(false);
		}
	};

	return (
		<div className="min-h-screen bg-linear-to-br from-gray-900 via-gray-800 to-gray-900 flex items-center justify-center p-6">
			<Card className="w-full max-w-md">
				<CardHeader>
					<div className="text-center space-y-2">
						<div className="flex justify-center mb-4">
							<div className="w-16 h-16 bg-red-600 rounded-full flex items-center justify-center">
								<LogIn className="w-8 h-8 text-white" />
							</div>
						</div>
						<h1 className="text-3xl font-bold text-gray-100">Trip Planner</h1>
						<p className="text-gray-400">
							Sign in to start planning your journey
						</p>
					</div>
				</CardHeader>
				<CardContent className="space-y-4">
					{error && (
						<div className="bg-red-900/20 border border-red-800 text-red-400 px-4 py-3 rounded-lg text-sm">
							{error}
						</div>
					)}

					<Button
						onClick={handleGoogleSignIn}
						disabled={loading}
						className="w-full"
						size="lg"
					>
						{loading ? (
							<>
								<div className="inline-block animate-spin rounded-full h-4 w-4 border-b-2 border-white mr-2" />
								Signing in...
							</>
						) : (
							<>
								<svg
									className="w-5 h-5 mr-2"
									viewBox="0 0 24 24"
									aria-label="Google logo"
								>
									<title>Google</title>
									<path
										fill="currentColor"
										d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
									/>
									<path
										fill="currentColor"
										d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
									/>
									<path
										fill="currentColor"
										d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
									/>
									<path
										fill="currentColor"
										d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
									/>
								</svg>
								Continue with Google
							</>
						)}
					</Button>

					<p className="text-center text-sm text-gray-500">
						By continuing, you agree to our Terms of Service and Privacy Policy
					</p>
				</CardContent>
			</Card>
		</div>
	);
}
